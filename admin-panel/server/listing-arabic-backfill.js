/**
 * Ensures activities + season_events have Arabic columns and backfills copy.
 * Express `/public/*` maps these to camelCase for the iOS app; without this,
 * Arabic mode still shows English on grid / nearby cards when not using Supabase.
 */
function backfillArabicListings(db) {
  const addCol = (table, col) => {
    const cols = db.prepare(`PRAGMA table_info(${table})`).all();
    if (cols.some((c) => c.name === col)) return;
    db.exec(`ALTER TABLE ${table} ADD COLUMN ${col} TEXT`);
  };
  for (const t of ['activities', 'season_events']) {
    addCol(t, 'arabic_name');
    addCol(t, 'arabic_category');
    addCol(t, 'arabic_description');
    addCol(t, 'arabic_location');
  }

  const updAct = db.prepare(`
    UPDATE activities
    SET arabic_name = @an, arabic_category = @ac, arabic_location = @al, arabic_description = @ad
    WHERE name = @name AND city = @city
  `);
  for (const r of activityArabicRows) {
    updAct.run(r);
  }

  const updEv = db.prepare(`
    UPDATE season_events
    SET arabic_name = @an, arabic_category = @ac, arabic_location = @al
    WHERE name = @name
  `);
  for (const r of seasonEventArabicRows) {
    updEv.run(r);
  }
}

